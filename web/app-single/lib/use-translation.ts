function readLocale(): string {
  if (typeof window === "undefined") return "en";
  try { const m = document.cookie.match(/(?:^|; )locale=([^;]*)/); if (m && (m[1] === "fr" || m[1] === "ar")) return m[1]; } catch {}
  const seg = window.location.pathname.split("/").filter(Boolean)[0];
  if (seg === "fr" || seg === "ar") return seg;
  return "en";
}

export function useTranslation() {
  const [locale, setLocale] = useState(readLocale);
  const [strings, setStrings] = useState<Record<string, string>>(() => translations[readLocale()] || translations.en);

  useEffect(() => {
    const loc = readLocale();
    if (loc !== locale) { setLocale(loc); setStrings(translations[loc] || translations.en); }
  }, []);

  const switchTo = (code: string) => {
    document.cookie = `locale=${code};path=/;max-age=${365 * 24 * 60 * 60}`;
    setLocale(code); setStrings(translations[code] || translations.en);
  };

  const t = (key: string, fallback?: string) => strings[key] || fallback || key;
  return { t, locale, switchTo };
}
